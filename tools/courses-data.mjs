/* Краткая сводка для главной страницы и клиентского рендера. */
export function coursesSummary(){
  return SUBJECTS.map(s=>({
    slug: s.slug,
    title: s.title,
    accent: s.accent || 'soc',
    kicker: s.kicker || '',
    tagline: s.tagline || '',
    names: s.names || '',
    total: (s.lectures||[]).length,
    ready: (s.lectures||[]).filter(l=>l.ready&&l.file).length,
    href: `/lectures/#subj-${s.slug}`,
  }));
}

/* Карта «фильм → лекция» для мостов на страницах рецензий.
   Название берётся из строк films («…» · «…»); ключ — без кавычек-ёлочек. */
export function filmLectureMap(){
  const map = new Map();
  for(const s of SUBJECTS){
    for(const l of (s.lectures||[])){
      for(const m of (l.films||'').matchAll(/«([^»]+)»/g)){
        const key = m[1].trim();
        if(!map.has(key)) map.set(key, {
          course: s.title, courseSlug: s.slug, n: l.n, lecture: l.title,
          ready: !!(l.ready && l.file),
          href: (l.ready && l.file) ? `/lectures/${s.slug}/${l.file}` : `/lectures/#subj-${s.slug}`,
        });
      }
    }
  }
  return map;
}

/* Карта «фильм → ВСЕ лекции», где он фигурирует (для указателя и хабов фильмов).
   В отличие от filmLectureMap (первая лекция для «моста»), возвращает массив:
   один фильм может разбираться в нескольких лекциях/курсах. */
export function filmLectureMapAll(){
  const map = new Map();
  for(const s of SUBJECTS){
    for(const l of (s.lectures||[])){
      for(const m of (l.films||'').matchAll(/«([^»]+)»/g)){
        const key = m[1].trim();
        if(!map.has(key)) map.set(key, []);
        map.get(key).push({
          course: s.title, courseSlug: s.slug, n: l.n, lecture: l.title,
          ready: !!(l.ready && l.file),
          href: (l.ready && l.file) ? `/lectures/${s.slug}/${l.file}` : `/lectures/#subj-${s.slug}`,
        });
      }
    }
  }
  return map;
}
